# att spara: Läs in en temperatur i Celsius och spara den i variabeln 'celsius'
celsius = float(input("Ange temperatur i Celsius: "))

# att spara: Räkna om Celsius till Fahrenheit och spara i variabeln 'fahrenheit'
fahrenheit = celsius * 9 / 5 + 32

# Skriv ut temperaturen i Fahrenheit
print("Temperaturen i Fahrenheit är:", fahrenheit)